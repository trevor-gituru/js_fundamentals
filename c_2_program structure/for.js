//It is a shorter more comprehensible version of while Loop
//In the loop state; it contains the counter dfn, loop condition, and counter update
for (let number = 0; number <= 12; number = number + 2) {
    console.log(number);
}
// → 0
// → 2
// …etcetera

let result = 1;
for (let counter = 0; counter < 10; counter = counter + 1) {
    result = result * 2;
}
console.log(result);
// → 1024
