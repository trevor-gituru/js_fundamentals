let counter = 0;
console.log(counter);
//->0
//To add one to a number
counter ++;
console.log(counter);
//->1
//To subtract one from a number
counter --;
console.log(counter);
//->0
//To add a given number to the binding
counter += 1;
console.log(counter);
//->1
//To subtract a given number to the binding
counter -= 1;
console.log(counter);
//->0
